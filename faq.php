<a href="#top"><img src="pic/arrowup.gif" border="0"></a><a name="56"></a> <b>Как сделать, чтоб мой торрент-клиент использовал прокси?</b><br />

Когда вы настраиваете прокси для Internet Explorer`a, вы фактически настраиваете прокси для всего http-траффика
