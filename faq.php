С другой стороны, если вы используете другой броузер (Opera/Mozilla/Firefox и т.д.)
и настраиваете в нем прокси - эти настройки будут действовать только на этот броузер.
Мы не знаем торрент клиенты, позволяющие настраивать прокси только для себя.</a><br /><br />
