<a href="#top"><img src="pic/arrowup.gif" border="0"></a><a name="0"></a> <b>Что такое торрент (bittorrent)? Как скачивать файлы?</b><br />
<b>Битторрент</b> - это протокол разработанный для обмена файлами. По сути он является peer-to-peer протоколом, когда каждый пользователь коннектится к другому напрямую, для приема или передачи частей информации. Но в то же время существует центральный сервер ( называемый трекер) который является координатором действий всех существующих связей между пользователями. Трекер только управляет соединениями, он не содержит никакой информации о содержимом файлов, передаваемых между пользователями. Принципиальное отличие Битторрент - от других p2p систем - в том , что пользователь, который скачивает к себе информацию, является так же ее распространителем, т.е передает информацию другим пользоваелям. В этом случае пропупскная способность сети используется наилучшим способом, насколько это возможно. Битторрент разработан для наилучшей работы в сетях, где большое количество людей с совпадающими интересами. При этом чем большее количество людей участвует в раздаче конкретного торрента, тем большую конечную скорость может получить каждый из них.<br>
