<a href="#top"><img src="pic/arrowup.gif" border="0"></a><a name="53"></a> <b>Как обнаружить, что я сижу за прокси?</b><br />

Попробуйте <a href=http://proxyjudge.org>ProxyJudge</a>. Он выдаст вам HTTP заголовки, которые получил сервер от вас.
Самые важные - это HTTP_CLIENT_IP, HTTP_X_FORWARDED_FOR и REMOTE_ADDR.</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0"></a><a name="54"></a> <b>Почему написано, что невозможно подключится, хотя я не использую NAT/firewall</b><br />
Наш трекер достаточно сообразительный относительно вопроса определения вашего реального IP,
однако, ему необходимо, чтобы прокси отсылал заголовок HTTP_X_FORWARDED_FOR. Если прокси вашего провайдера этого не
делает - происходит следующее: трекер интерпретирует IP прокси, как ваш собственный. И когда вы пытаетесь зайти на трекер,
он пытается соединится с вашим клиентом, что бы определить, сидите ли вы за NAT/firewall, однако на самом деле он коннектиться к
прокси-серверу, по порту, который вы указали в своём клиенте. Т.к. прокси ничего не принимает по данному порту, следовательно соединение
не будет установлено, и трекер будет думать, что вы за натом/стенкой.</a><br /><br />
